//ciclos en JS
//WHILE
let contador = 0;

while (contador < 3){
    console.log(contador);
    contador++;
}

console.log("fin ciclo while")

//DO WHILE

let pepito = 0;

do{
    console.log(pepito);
    pepito++;
}while (pepito < 3);

console.log ("fin ciclo DO while");

//CICLO FOR
//declaracion variable; condicion (si es v ejecuta); contador
for(let cuenta = 0, x = 0, y = 0; cuenta < 3; cuenta++){
    console.log(cuenta)
}

console.log("fin del ciclo FOR")